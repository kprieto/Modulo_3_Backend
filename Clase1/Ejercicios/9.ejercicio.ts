/*Ejercicio 9: Comparaciones lógicas 
Declara dos variables booleanas y realiza una comparación lógica (como AND, 
OR). Imprime el resultado. */

let valorTrue : boolean = true;
let valorFalse: boolean = false;

let comparacionOR = valorTrue || valorFalse;
console.log(`Comparación OR: ${comparacionOR}`);

let comparacionAND = valorTrue && valorFalse;
console.log(`Comparación AND: ${comparacionAND}`);